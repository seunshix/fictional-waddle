// Identifiers

	/*
		A valid identifier is a sequence of one or more letters, digits, or underscore characters (_). 
		Spaces, punctuation marks, and symbols cannot be part of an identifier.
		In addition, identifiers shall always begin with a letter.
		They can also begin with an underline character (_), but such identifiers are -on most cases- considered
		reserved for compiler-specific keywords or external identifiers, as well as identifiers containing two successive underscore characters anywhere.
		In no case can they begin with a digit.
	*/